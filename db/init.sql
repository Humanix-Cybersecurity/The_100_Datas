DROP TABLE IF EXISTS logs, predictions, habitants CASCADE;

CREATE TABLE habitants (
  id        INTEGER PRIMARY KEY,
  nom       TEXT NOT NULL,
  station   TEXT NOT NULL
);

CREATE TABLE predictions (
  prediction_id INTEGER PRIMARY KEY,
  habitant_id   INTEGER REFERENCES habitants(id),
  score_survie  INTEGER,
  verdict       TEXT
);

CREATE TABLE logs (
  log_id        INTEGER PRIMARY KEY,
  prediction_id INTEGER REFERENCES predictions(prediction_id),
  action        TEXT,
  auteur        TEXT,
  horodatage    TIMESTAMP
);

